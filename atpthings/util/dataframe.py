def add_one(number: int) -> int:
    """Add one

    :param number: write number
    :type number: int
    :return: return the number
    :rtype: int
    """

    return number + 1


def add_two(number: int) -> int:
    """Add two

    :param number: write number
    :type number: int
    :return: return the number
    :rtype: int
    """

    return number + 2
